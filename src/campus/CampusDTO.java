package campus;

public class CampusDTO {
	public String campusNum;
	public String campusKorName;
	public String campusEngName;
	
	public CampusDTO() {}
	
	public CampusDTO(String campusNum, String campusKorName, String campusEngName) {
		this.campusNum = campusNum;
		this.campusKorName = campusKorName;
		this.campusEngName = campusEngName;
	}
	
	
}
